<template>
  <div class="login">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
    >
      <h3 class="title">若依后台管理系统</h3>
      <el-form-item prop="account">
        <el-input
          v-model="loginForm.account"
          type="text"
          auto-complete="off"
          placeholder="账号"
        >
          <svg-icon
            slot="prefix"
            icon-class="user"
            class="el-input__icon input-icon"
          />
        </el-input>
      </el-form-item>
      <el-form-item prop="pwd">
        <el-input
          v-model="loginForm.pwd"
          type="password"
          auto-complete="off"
          placeholder="密码"
          @keyup.enter.native="handleLogin"
        >
          <svg-icon
            slot="prefix"
            icon-class="password"
            class="el-input__icon input-icon"
          />
        </el-input>
      </el-form-item>
      <el-form-item prop="imgcode">
        <el-input
          v-model="loginForm.imgcode"
          auto-complete="off"
          placeholder="验证码"
          style="width: 63%"
          @keyup.enter.native="handleLogin"
        >
          <svg-icon
            slot="prefix"
            icon-class="validCode"
            class="el-input__icon input-icon"
          />
        </el-input>
        <div class="login-code">
          <img :src="codeUrl" @click="getCode" class="login-code-img" />
        </div>
      </el-form-item>
      <el-checkbox
        v-model="loginForm.rememberMe"
        style="margin: 0px 0px 25px 0px"
        >记住密码</el-checkbox
      >
      <el-form-item style="width: 100%">
        <el-button
          :loading="loading"
          size="medium"
          type="primary"
          style="width: 100%"
          @click.native.prevent="handleLogin"
        >
          <span v-if="!loading">登 录</span>
          <span v-else>登 录 中...</span>
        </el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
import { getCodeImg, login } from "@/api/login";
import Cookies from "js-cookie";
import { encrypt, decrypt } from "@/utils/jsencrypt";

export default {
  name: "Login",
  data() {
    var validateUsername = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入账号"));
      } else {
        callback();
      }
    };
    var validatePassword = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入密码"));
      } else {
        callback();
      }
    };
    var validateCode = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入验证码"));
      } else {
        callback();
      }
    };
    return {
      codeUrl: "",
      cookiePassword: "",
      loginForm: {
        account: "",
        pwd: "",
        rememberMe: false,
        imgcode: "",
        uuid: "",
      },
      loginRules: {
        account: [
          {
            required: true,
            trigger: "blur",
            message: "请输入您的账号",
          },
          {
            validator: validateUsername,
            trigger: "change",
          },
        ],
        pwd: [
          {
            required: true,
            trigger: "blur",
            message: "请输入您的密码",
          },
          {
            validator: validatePassword,
            trigger: "change",
          },
        ],
        imgcode: [
          {
            required: true,
            trigger: "change",
            message: "请输入验证码",
          },
          {
            validator: validateCode,
            trigger: "change",
          },
        ],
      },
      loading: false,
    };
  },
  watch: {
    $route: {
      handler: function (route) {
        this.redirect = route.query && route.query.redirect;
      },
      immediate: true,
    },
  },
  created() {
    this.getCode();
    this.getCookie();
    
    if(JSON.parse(localStorage.getItem('userInfo'))){
      this.loginForm.account=JSON.parse(localStorage.getItem('userInfo')).account
      this.loginForm.pwd=JSON.parse(localStorage.getItem('userInfo')).pwd
    } 
  },
  methods: {
    getCode() {
      this.codeUrl =
        process.env.VUE_APP_BASE_API + `/adminapi/captcha_pro?${Date.now()}`;
    },
    getCookie() {},
    handleLogin() {
      this.$store.dispatch('user/Login',this.loginForm).then(res=>{
        console.log('denglu',res);
        if(res.status===200){
          //this.$router.push('/')
        }
      })
    },
  },
};
</script>

